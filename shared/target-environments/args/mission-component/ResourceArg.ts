/**
 * Provides static keys for a `"resource"` mission-component argument.
 */
export class ResourceArg {
  /**
   * The key used in the effect's arguments to reference the resource's ID.
   */
  public static readonly RESOURCE_ID = 'resourceId'
  /**
   * The key used in the effect's arguments to reference the resource's name.
   */
  public static readonly RESOURCE_NAME = 'resourceName'
}
